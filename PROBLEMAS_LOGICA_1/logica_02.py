# Operaciones basicas de python

def operaciones_basicas():
    a = float(input("Ingrese el primer número: "))
    b = float(input("Ingrese el segundo número: "))
    print("Producto:", a * b)
    print("Módulo:", a % b)
    print("Cociente:", a / b)
    print("Suma:", a + b)
    print("Resta:", a - b)
